<?php
// Внешний цикл: перебираем числа от 1 до 10 (первый множитель)
for ($i = 1; $i <= 10; $i++) {
    // Внутренний цикл: перебираем числа от 1 до 10 (второй множитель)
    for ($j = 1; $j <= 10; $j++) {
        // Вычисляем произведение и выводим результат
        echo $i . " x " . $j . " = " . ($i * $j) . "\t";
    }
    echo "\n"; // Переход на новую строку после завершения внутреннего цикла
}
