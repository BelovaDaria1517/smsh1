<?php
// Внешний цикл: перебираем числа от 2 до 100
for ($number = 2; $number <= 100; $number++) {
    $isPrime = true; // Предполагаем, что число простое

    // Внутренний цикл: проверяем, делится ли число на что-то кроме 1 и себя
    for ($i = 2; $i <= sqrt($number); $i++) {
        if ($number % $i == 0) {
            $isPrime = false; // Если делится, число не простое
            break; // Выходим из внутреннего цикла
        }
    }

    // Если число простое, выводим его
    if ($isPrime) {
        echo $number . " ";
    }
}
