<?php
// Запрашиваем у пользователя число
echo "Введите число для вычисления факториала: ";
$number = intval(trim(fgets(STDIN))); // Чтение числа из консоли

// Инициализация переменных
$factorial = 1;
$i = 1;

// Проверка на отрицательное число
if ($number < 0) {
    echo "Факториал отрицательного числа не определен.\n";
} else {
    // Вычисление факториала с помощью цикла while
    while ($i <= $number) {
        $factorial *= $i; // Умножаем текущий результат на i
        $i++; // Увеличиваем i на 1
    }

    // Вывод результата
    echo "Факториал числа $number равен $factorial\n";
}